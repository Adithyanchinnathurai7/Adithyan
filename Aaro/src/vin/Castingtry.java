package vin;

public class Castingtry {

	public static void main(String[] args) {
		int n=100;
		byte d;
		
		d=(byte)n;
		System.out.println(d);
		 
		n=578;
		d=(byte)n;
		System.out.println(d);
		
		byte n1=20;
		byte n2=30;
		System.out.println(n1*n2);
		
		byte get=(byte)(n1*n2);
		
		float f =27.8f;
		n=(int)f;
		System.out.println(n);
		f=n;
		System.out.println(f);
		
		
	}

}
